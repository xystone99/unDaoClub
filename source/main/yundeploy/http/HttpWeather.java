	/**
	 * 今日天气
	 */
	public static final String reg_WeatherToday =
		"<ul class=\"clearfix\">"
		+ "(.*?)<h1>(.{0,20})</h1>"																	//2,时间1-2
		+ "(.*?)<p class=\"wea\" title=\"(.{0,20})\">(.{0,20})</p>"									//3,天气1-5
		+ "(.*?)<span>(.{0,20})</span><em>"															//2,温度1-7	
		+ "(.*?)<span class=\"\" title=\"(.{0,20})\">(.{0,20})</span>"								//3,风度1-10
		+ "(.*?)<h1>(.{0,20})</h1>"																	//2,时间2-12
		+ "(.*?)<p class=\"wea\" title=\"(.{0,20})\">(.{0,20})</p>"									//3,天气2-15
		+ "(.*?)<span>(.{0,20})</span><em>"															//2,温度2-17	
		+ "(.*?)<span class=\"\" title=\"(.{0,20})\">(.{0,20})</span></p>";							//3,风度2-20
	public static final Pattern p_WeatherToday = Pattern.compile( reg_WeatherToday, Pattern.CASE_INSENSITIVE );

	public final static String[] getTodayWeather( String city ) {
		if ( !mapCity.containsKey(city) ) {
			return null;
		}
		StringBuilder bufURL = new StringBuilder();
		bufURL.append( "http://www.weather.com.cn/weather1d/" ).append( mapCity.get(city) ).append( ".shtml" );
		String q_Result = HttpParser.queryContentGET( bufURL.toString(), null, CHAR_SET, null );
		Matcher m_Weather = p_WeatherToday.matcher( q_Result );
		StringBuilder bufRemark = new StringBuilder();
		String[] qryResult = new String[6]; 
		if ( m_Weather.find() ) {
			bufRemark.append( m_Weather.group(2) ).append( ",&nbsp;&nbsp;" ).append( m_Weather.group(5) ).append( ",&nbsp;&nbsp;" ).append( m_Weather.group(7) ).append( "°C,&nbsp;&nbsp;" ).append( m_Weather.group(10) ).append( ",&nbsp;&nbsp;&nbsp;&nbsp;" );
			bufRemark.append( m_Weather.group(12) ).append( ",&nbsp;&nbsp;" ).append( m_Weather.group(15) ).append( ",&nbsp;&nbsp;" ).append( m_Weather.group(17) ).append( "°C,&nbsp;&nbsp;" ).append( m_Weather.group(20) );
			qryResult[0] = m_Weather.group(5);
			qryResult[1] = m_Weather.group(15);
			qryResult[2] = m_Weather.group(7);
			qryResult[3] = m_Weather.group(17);
			qryResult[4] = m_Weather.group(20);
			qryResult[5] = bufRemark.toString();
			
		}
		return qryResult;
	} 
	
