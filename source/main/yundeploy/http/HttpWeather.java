		"<ul class=\"t clearfix\">"
		+ "(.*?)<h1>XXX(.{0,20})</h1>"																//2,XXX应替换为日期
		+ "(.*?)<p title=\"(.{0,20})\" class=\"wea\">(.{0,20})</p>"									//3,天气-5
		+ "(.*?)<span>(.{0,10})</span>/<i>(.{0,10})℃</i>"											//3,温度-7/8
		+ "(.*?)<span title=\"(.{0,20})\" class=\"(.{0,20})\">"										//3,风向-10
		+ "(.*?)<span title=\"(.{0,20})\" class=\"(.{0,20})\">"										//3,风向-13
		+ "(.*?)<i>(.{0,20})</i>(.{0,20})</p>";														//3,风力-16				

	public final static String[] getWeatherInfo( String city, String date ) {
		if ( !mapCity.containsKey(city) ) {
			return null;
		}
		if ( DateUtils.formatCurrentDate().equals(date) ) {
			return getTodayWeather( city );
		}
		Calendar cal = DateUtils.buildCalendar(date);
		String reg_Weather = reg_WeatherFuture.replaceFirst("XXX", cal.get(Calendar.DAY_OF_MONTH)+"日" );
		Pattern p_WeatherFuture = Pattern.compile( reg_Weather, Pattern.CASE_INSENSITIVE );
		
		StringBuilder bufURL = new StringBuilder();
		bufURL.append( "http://www.weather.com.cn/weather/" ).append( mapCity.get(city) ).append( ".shtml" );
		String q_Result = HttpParser.queryContentGET( bufURL.toString(), null, CHAR_SET, null );
		Matcher m_Weather = p_WeatherFuture.matcher( q_Result );
		StringBuilder bufRemark = new StringBuilder();
		String[] qryResult = new String[6]; 
		if ( m_Weather.find() ) {
			bufRemark.append( m_Weather.group(5) ).append( ",&nbsp;&nbsp;" ).append( m_Weather.group(7) ).append( "&nbsp;/&nbsp;" ).append( m_Weather.group(8) ).append( "°C,&nbsp;&nbsp;" );
			bufRemark.append( m_Weather.group(10) ).append( "&nbsp;/&nbsp;" ).append( m_Weather.group(13) ).append( "&nbsp;&nbsp;" ).append( m_Weather.group(16) );
			String[] tq = m_Weather.group(5).split( "转" );
			qryResult[0] = tq[0];
			qryResult[1] = tq.length>1?tq[1]:tq[0];
			qryResult[2] = m_Weather.group(7);
			qryResult[3] = m_Weather.group(8);
			qryResult[4] = m_Weather.group(16);
			qryResult[5] = bufRemark.toString();
		}
		return qryResult;
	} 
	
